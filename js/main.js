function showPoemModal(index) {
    const poem = portfolioData.poetry[index];
    const modal = document.getElementById('poemModal');
    const modalContent = document.getElementById('modalContent');
    modalContent.innerHTML = `
        <h2 class="${poem.language}">${poem.title}</h2>
        <div class="poetry-content ${poem.language}">
            ${poem.content.split('\n\n').map(stanza => 
                `<p>${stanza.split('\n').join('<br>')}</p>`
            ).join('')}
        </div>
    `;
    modal.style.display = 'block';
}

function closePoemModal() {
    document.getElementById('poemModal').style.display = 'none';
}

// Add event listener to close modal when clicking outside
window.onclick = function(event) {
    const modal = document.getElementById('poemModal');
    if (event.target == modal) {
        modal.style.display = 'none';
    }
}
