document.addEventListener('DOMContentLoaded', () => {
    // Initialize AOS
    AOS.init({
        duration: 800,
        once: true
    });

    // Add scroll event for navbar
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            document.getElementById('navbar').classList.add('scrolled');
        } else {
            document.getElementById('navbar').classList.remove('scrolled');
        }
    });

    // Add scroll-to-top functionality
    const scrollToTopButton = document.getElementById('scrollToTop');
    
    window.addEventListener('scroll', () => {
        if (window.scrollY > 300) {
            scrollToTopButton.classList.add('visible');
        } else {
            scrollToTopButton.classList.remove('visible');
        }
    });

    scrollToTopButton.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });

    // Add keyboard navigation
    const sections = portfolioData.navigation.links.map(link => link.toLowerCase().replace(/\s+/g, '-'));
    sections.unshift('hero'); // Add hero section at the beginning

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Tab' || e.key === 'Enter') {
            e.preventDefault();
            const currentSection = window.location.hash.slice(1) || 'hero';
            const currentIndex = sections.indexOf(currentSection);
            const nextIndex = (currentIndex + 1) % sections.length;
            
            window.location.hash = `#${sections[nextIndex]}`;
            document.getElementById(sections[nextIndex]).scrollIntoView({ behavior: 'smooth' });
        }
    });

    // Handle initial hash in URL
    if (window.location.hash) {
        const section = window.location.hash.slice(1);
        if (sections.includes(section)) {
            document.getElementById(section).scrollIntoView({ behavior: 'smooth' });
        }
    }

    try {
        renderNavbar(portfolioData.navigation);
        renderHero(portfolioData.hero);
        renderAbout(portfolioData.about);
        renderSkills(portfolioData.skills);
        renderExperience(portfolioData.experience);
        renderProjects(portfolioData.projects);
        renderEducation(portfolioData.education);
        renderCertifications(portfolioData.certifications);
        renderCreativeWorks(portfolioData.poetry);
    } catch (error) {
        console.error('Error rendering portfolio:', error);
    }
});

